package iteracao_for;

public class Exercicio1 {

    public static void main(String[] args) {

        int[] valores = new int[] { 5, 10, 15, 20, 25 };

        System.out.println("===============================");

        for (int i = 0; i < valores.length; i = i + 1) {

            System.out.println(valores[i]);
        }


        System.out.println("===============================");
    }

}
